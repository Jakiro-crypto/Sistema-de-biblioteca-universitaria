    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
    
try {
            // TODO add your handling code here:
            String id_bibliotecario = this.txtid_bibliotecario.getText();
            String consultar = "SELECT * FROM bibliotecario WHERE id_bibliotecario = '"+id_bibliotecario+"'";
            Statement st = cn.conectar().createStatement();
            ResultSet rs = st.executeQuery(consultar);
            if(rs.next()){
                this.txtid_bibliotecario.setText(rs.getString("id_bibliotecario"));
                this.txtnombres.setText(rs.getString("nombres"));
                this.txtapellidos.setText(rs.getString("apellidos"));
                this.txtelefono.setText(rs.getString("telefono"));
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(FrmBibliotecario.class.getName()).log(Level.SEVERE, null, ex);
        }    // TODO add your handling code here:
    }//GEN-LAST:event_BuscarActionPerformed

