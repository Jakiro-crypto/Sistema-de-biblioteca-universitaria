    private void btn_buscarEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarEditActionPerformed
    try {
            // TODO add your handling code here:
            String id_editorial = this.txt_idEditorial.getText();
            String consultar = "SELECT *FROM editorial WHERE id_editorial = '"+id_editorial+"'";
            Statement st = cn.conectar().createStatement();
            ResultSet rs = st.executeQuery(consultar);
            if(rs.next()){
                this.txt_nombreEdit.setText(rs.getString("nombre"));
                this.txt_direccionEdit.setText(rs.getString("direccion"));
                this.txt_contactoEdit.setText(rs.getString("contacto"));
          
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(FrmEditorial.class.getName()).log(Level.SEVERE, null, ex);
        }     // TODO add your handling code here:
    }//GEN-LAST:event_btn_buscarEditActionPerformed

