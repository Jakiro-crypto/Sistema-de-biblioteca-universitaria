    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        // TODO add your handling code here:
        int row = Tabla.getSelectedRow();
        String ID_Multa = (String) Tabla.getValueAt(row, 0);
        String DNI = this.txt_DNI.getText();
        String Motivo = this.cbMotivo.getSelectedItem().toString();
        String Monto = this.txt_Monto.getText();
        String Estado = this.cbEstado.getSelectedItem().toString();
        String editar = "UPDATE multa SET DNI='" + DNI + "', Motivo='" + Motivo + "', Monto='" + Monto + "', Estado='" + Estado + "' WHERE ID_Multa=" + ID_Multa;
        try{
        Statement st = cn.conectar().createStatement();
        st.executeUpdate(editar);
        }catch(SQLException ex){
            Logger.getLogger(FrmMulta.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Multa modificada correctamente.");
        ListarMultas();
        Limpiar();
    }//GEN-LAST:event_EditarActionPerformed

    public void Limpiar()
    {
       this.txt_DNI.setText("");
       this.txt_Monto.setText("");
       this.cbMotivo.setSelectedIndex(-1);
       this.cbEstado.setSelectedIndex(-1); 
    }
    public void ListarMultas()
    {
    try {
        limpiarTabla();
        String listar = "SELECT * FROM multa ORDER BY ID_Multa";
        Statement st = cn.conectar().createStatement();
        ResultSet rs = st.executeQuery(listar);
        modelo = (DefaultTableModel) this.Tabla.getModel();
        
        while (rs.next()) {
            Object[] persona = new Object[5]; 
            persona[0] = rs.getString("ID_Multa");
            persona[1] = rs.getString("DNI");
            persona[2] = rs.getString("Motivo");
            persona[3] = rs.getString("Monto");
            persona[4] = rs.getString("Estado");
            modelo.addRow(persona);
        }
        Tabla.setModel(modelo);
    } catch (SQLException e) {
        Logger.getLogger(FrmMulta.class.getName()).log(Level.SEVERE, null, e); // Manejar la excepción
    }
    }
    void limpiarTabla(){
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    private void AñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AñadirActionPerformed
     try {
        String DNI = this.txt_DNI.getText();
        String Motivo = this.cbMotivo.getSelectedItem().toString();
        String Monto = this.txt_Monto.getText();
        String Estado = this.cbEstado.getSelectedItem().toString();
        String insertar = "INSERT INTO multa (DNI,Motivo,Monto,Estado) VALUES('" + DNI + "','" + Motivo + "','" + Monto + "','" + Estado + "')";
        Statement st = cn.conectar().createStatement();
        int rs = st.executeUpdate(insertar);
            if (rs > 0) {
                JOptionPane.showMessageDialog(null, "Se agregó la multa correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo agregar la multa correctamente.");
            }
        ListarMultas();
        Limpiar();
    } catch (SQLException ex) {
        Logger.getLogger(FrmPrestamo.class.getName()).log(Level.SEVERE, null, ex);
    } 
    }//GEN-LAST:event_AñadirActionPerformed

