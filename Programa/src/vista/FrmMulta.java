    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        // TODO add your handling code here:
        int row = Tabla.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "No se seleccionó");
        }else{
            String DNI = (String) Tabla.getValueAt(row, 1);
            String Motivo = (String) Tabla.getValueAt(row, 2);
            String Monto = (String) Tabla.getValueAt(row, 3);
            String Estado = (String) Tabla.getValueAt(row, 4);
            txt_Monto.setText(Monto);
            txt_DNI.setText(DNI);
            cbMotivo.setSelectedItem(Motivo);
            cbEstado.setSelectedItem(Estado);
        }
    }//GEN-LAST:event_TablaMouseClicked

